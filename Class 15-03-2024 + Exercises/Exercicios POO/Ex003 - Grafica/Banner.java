public class Banner extends Product {
    public String bannerType; 
    public String bannerMaterial; 
    public String bannerTemplate;
    
    public String getBannerType() {
        return bannerType;
    }
    public void setBannerType(String bannerType) {
        this.bannerType = bannerType;
    }
    public String getBannerMaterial() {
        return bannerMaterial;
    }
    public void setBannerMaterial(String bannerMaterial) {
        this.bannerMaterial = bannerMaterial;
    }
    public String getBannerTemplate() {
        return bannerTemplate;
    }
    public void setBannerTemplate(String bannerTemplate) {
        this.bannerTemplate = bannerTemplate;
    } 
}
